		public CustomViewModel()
		{
			GetIcon();
			GetColor();

			var assembly = typeof(CustomViewModel).GetTypeInfo().Assembly;
			Stream stream = null;
#if COMMONSB
            stream = assembly.GetManifestResourceStream("SampleBrowser.Samples.AutoComplete.CustomSearch.txt");
#else
            stream = assembly.GetManifestResourceStream("SampleBrowser.SfAutoComplete.CustomSearch.txt");
#endif
            string text = "";
			using (var reader = new System.IO.StreamReader(stream))
			{
				while ((text = reader.ReadLine()) != null)
				{
					string[] splits = text.Split('*');

					if (splits.Length == 3)
					{

						try
						{
							CustomModel model = new CustomModel();
							model.Name = splits[0];
							model.Unicode = s[splits[1]];
							model.Tag = splits[2];
							model.BackColor = PickColor();
							DataCollection.Add(model);
						}
						catch (Exception e)
						{
							var ee = e.Message;
						}

					}
				}
			}
			FilteredCollection = DataCollection;


		}

        public Color PickColor()
        {
            ColorCount++;
            if ((Device.RuntimePlatform == Device.Android) || (Device.RuntimePlatform == Device.iOS))
            {
                if (ColorCount >= 16)
                    ColorCount = 0;
            }
            else
            {
                if (ColorCount >= 15)
                    ColorCount = 0;
            }
            return ColorCode[ColorCount];
        }

