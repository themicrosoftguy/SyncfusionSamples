		public bool CustomSearchLogic(object value, object actualText)
		{
			var tags = (actualText as CustomModel).Tag.Split(',');

			var iteratedItem = value.ToString().ToLower();
			foreach (var item in tags)
			{
				var originalItem = item.Replace(" ", "").ToLower();

				if (originalItem.Contains(iteratedItem))
				{
					(actualText as CustomModel).BackColor = PickColor();
					return true;
				}
			}

			return false;
		}

